LIBRARY IEEE;
USE IEEE.STD_LOGIC_1164.ALL;

ENTITY CM IS
PORT(A,B: IN STD_LOGIC_VECTOR(3 DOWNTO 0);
	I,MA,ME: OUT STD_LOGIC
	);
END ENTITY;

ARCHITECTURE A_CM OF CM IS
BEGIN
	PROCESS(A,B)
	BEGIN
		I<='0';
		MA<='0';
		ME<='0';
		IF(A=B) THEN
			I<='1';
		ELSIF(A>B) THEN
			MA<='1';
		ELSE
			ME<='1';
		END IF;
	END PROCESS;
END A_CM;
